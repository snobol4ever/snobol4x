/* =========================================================================
 * Output helpers
 * ======================================================================= */

static FILE *pl_out;
static int   pl_uid_ctr = 0;
static int   pl_next_uid(void) { return ++pl_uid_ctr; }

static void PL_C(const char *fmt, ...) {
    va_list ap; va_start(ap, fmt); vfprintf(pl_out, fmt, ap); va_end(ap);
}

/* Emit a C string literal body with proper escaping */
static void emit_c_string(const char *s) {
    for (; *s; s++) {
        if      (*s == '"')                  PL_C("\\\"");
        else if (*s == '\\')                 PL_C("\\\\");
        else if (*s == '\n')                 PL_C("\\n");
        else if (*s == '\r')                 PL_C("\\r");
        else if (*s == '\t')                 PL_C("\\t");
        else if ((unsigned char)*s < 32)     PL_C("\\x%02x", (unsigned char)*s);
        else                                 PL_C("%c", *s);
    }
}


