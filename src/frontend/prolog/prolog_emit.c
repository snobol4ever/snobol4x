/* =========================================================================
 * Output helpers
 * ======================================================================= */

static FILE *pl_out;
static int   pl_uid_ctr = 0;
static int   pl_next_uid(void) { return ++pl_uid_ctr; }

static void PL_C(const char *fmt, ...) {
    va_list ap; va_start(ap, fmt); vfprintf(pl_out, fmt, ap); va_end(ap);
}

/* Use the same 3-column pretty-printer as the C backend */
#define PRETTY_OUT pl_out
#include "emit_pretty.h"

/* =========================================================================
 * Label buffer size
 * ======================================================================= */
#define LBUF 128

/* =========================================================================
 * C-safe identifier from an atom name
 * ======================================================================= */
static char *safe_name(const char *s) {
    static char buf[256];
    int j = 0;
    buf[j++] = 'p'; buf[j++] = '_';  /* prefix to avoid C keyword clashes */
    for (int i = 0; s[i] && j < 250; i++) {
        unsigned char c = (unsigned char)s[i];
        buf[j++] = (isalnum(c) || c == '_') ? (char)c : '_';
    }
    buf[j] = '\0';
    return buf;
}

