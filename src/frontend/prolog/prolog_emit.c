/*
 * pl_emit.c — Prolog IR -> C code emitter (Byrd box four-port model)
 *
 * Implements the Proebsting technique from:
 *   "Simple Translation of Goal-Directed Evaluation" (Todd A. Proebsting, 1996)
 *
 * Each IR node kind gets four labeled code chunks: α β γ ω
 *   α  (α / start)  — initial entry, try-head
 *   β  (β  / resume) — re-entry after backtrack, try next clause
 *   γ  (γ / succeed)— departure on success
 *   ω  (ω / fail)   — departure on failure / all clauses exhausted
 *
 * Byrd Box wiring for clause selection (per FRONTEND-PROLOG.md):
 *
 *   P_FOO_α:                             ← entry (E_TRAIL_MARK, try clause 1 head)
 *       if (!unify(arg, head1)) goto P_FOO_β;
 *       <body clause 1>
 *       goto ret_γ;
 *
 *   P_FOO_β:                             ← backtrack (E_TRAIL_UNWIND, try clause 2)
 *       trail_unwind(&_trail, _mark);
 *       if (!unify(arg, head2)) goto P_FOO_ω;
 *       <body clause 2>
 *       goto ret_γ;
 *
 *   P_FOO_ω:                             ← all exhausted
 *       trail_unwind(&_trail, _mark);
 *       goto ret_ω;
 *
 * Cut (E_CUT) seals β → ω.  Exactly FENCE semantics.
 *
 * Entry point: pl_emit(Program*, FILE*)
 *
 * Build notes:
 *   This file is included by the sno2c driver when -pl is active.
 *   It #includes emit_pretty.h from the C backend for the PLG/PL/PS/PG macros.
 *   Compile with:
 *     gcc -I src/frontend/snobol4 -I src/frontend/prolog \
 *         -I src/backend/c pl_emit.c prolog_atom.c -c
 */

