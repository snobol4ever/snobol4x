#!/usr/bin/env python3
"""
build_expr_evaluator.py — Sprint 18

Builds a compiled expression evaluator by extending the Sprint 16
expression parser with value stack operations.

The grammar is identical to Expressions.py. The difference:
every pattern that produces a value pushes it onto the runtime
value stack (sno_vpush/sno_vpop). Operators pop their operands,
compute, push the result.

Mapping from Expressions.py evaluate() to value stack:

  parse_item() yields int(_1)     → sno_vpush(atoi(matched))
  parse_item() yields 'x'         → sno_vpush(10)
  parse_item() yields 'y'         → sno_vpush(20)
  parse_item() yields 'z'         → sno_vpush(30)
  parse_item() yields _2 (parens) → child already pushed TERM result

  parse_element() yields _1       → item value already on stack
  parse_element() yields ('+',_2) → a = sno_vpop(); sno_vpush(+a)
  parse_element() yields ('-',_2) → a = sno_vpop(); sno_vpush(-a)

  parse_factor() yields _1        → element value already on stack
  parse_factor() yields ('*',l,r) → r=pop(); l=pop(); push(l*r)
  parse_factor() yields ('/',l,r) → r=pop(); l=pop(); push(r==0?0:l//r)

  parse_term() yields _1          → factor value already on stack
  parse_term() yields ('+',l,r)   → r=pop(); l=pop(); push(l+r)
  parse_term() yields ('-',l,r)   → r=pop(); l=pop(); push(l-r)

The C is emitted by hand — not through emit_c.py — because the value
stack operations need to be woven into the match_ok paths of each
alternative, which the IR doesn't support yet.

Sprint 19 will add Compute nodes to the IR to make this general.
"""

import sys, os, subprocess, tempfile

HERE    = os.path.dirname(os.path.abspath(__file__))
ROOT    = os.path.join(HERE, '..', '..')
RUNTIME = os.path.abspath(os.path.join(ROOT, 'src', 'runtime'))


