def build_evaluator(out_path=None):
    src = EVALUATOR_C.replace('RUNTIME_H', f'{RUNTIME}/runtime.h')

    with tempfile.NamedTemporaryFile(suffix='.c', mode='w', delete=False) as f:
        f.write(src)
        c_path = f.name

    if out_path is None:
        out_path = c_path.replace('.c', '')

    result = subprocess.run(
        ['cc', '-O2', '-o', out_path, c_path,
         os.path.join(RUNTIME, 'runtime.c'), f'-I{RUNTIME}'],
        capture_output=True, text=True
    )
    os.unlink(c_path)

    if result.returncode != 0:
        print("Compile error:", file=sys.stderr)
        print(result.stderr, file=sys.stderr)
        return None

    return out_path


